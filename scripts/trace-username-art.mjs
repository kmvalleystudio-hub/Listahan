/**
 * Legacy helper: traces a bitmap into filled paths (usernameWritingPath.ts).
 * The app now uses hand-drawn strokes in src/constants/usernameWritingArt.ts instead.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import potrace from "potrace";
import sharp from "sharp";

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), "..");
const input =
  process.argv[2] ||
  "C:/Users/admin/.cursor/projects/c-Users-admin-cursor-projects-SayCart/assets/c__Users_admin_AppData_Roaming_Cursor_User_workspaceStorage_empty-window_images_image-88b1428e-9059-4899-91af-ca31bce544aa.png";
const tmp = path.join(root, "assets/illustrations/_trace-temp.png");
const outTs = path.join(root, "src/constants/usernameWritingPath.legacy.ts");

const meta = await sharp(input).metadata();
const w = meta.width || 800;
const h = meta.height || 600;
const cropW = Math.round(w * 0.88);
const cropH = Math.round(h * 0.82);
const left = Math.round((w - cropW) / 2);
const top = Math.round((h - cropH) * 0.08);

await sharp(input)
  .extract({ left, top, width: cropW, height: cropH })
  /** Larger trace canvas = thinner lines when cover-scaled to the phone (small viewBox = huge on screen). */
  .resize(260, null, { fit: "inside" })
  .greyscale()
  .normalise()
  .threshold(252)
  .png()
  .toFile(tmp);

const svg = await new Promise((resolve, reject) => {
  potrace.trace(
    tmp,
    { turdSize: 1, optTolerance: 0.08, threshold: 128, color: "#000000", background: "transparent" },
    (err, result) => (err ? reject(err) : resolve(result))
  );
});

const viewBoxMatch = svg.match(/viewBox="([^"]+)"/);
const pathMatch = svg.match(/d="([^"]+)"/);
if (!viewBoxMatch || !pathMatch) throw new Error("Could not parse potrace SVG");

const viewBox = viewBoxMatch[1];
const d = pathMatch[1];

fs.writeFileSync(
  outTs,
  `/** Auto-generated by scripts/trace-username-art.mjs — thin vector trace of writing illustration */\nexport const USERNAME_WRITING_VIEWBOX = ${JSON.stringify(viewBox)};\nexport const USERNAME_WRITING_PATH = ${JSON.stringify(d)};\n`
);

fs.writeFileSync(path.join(root, "assets/illustrations/username-writing-one-line.svg"), svg);
console.log("Wrote", outTs, "viewBox", viewBox, "path chars", d.length);
